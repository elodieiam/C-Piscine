char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	while (dest[i])
		i++;
	j = 0;
	while (src[j])
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}

 #include <stdio.h>
int main()
{
char dest[] = "abc";
char src[] = "def";
printf("%s", ft_strcat(dest, src));
}
